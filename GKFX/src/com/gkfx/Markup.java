package com.gkfx;

public class Markup {
	private float	m_fx;
	private float	m_commodities;
	private float	m_index;
	private	float	m_all;
	
	private float	m_dollars;
	
	public Markup(float fx, float commodities, float index, float all, float dollars){
		this.m_fx			=	fx;
		this.m_commodities	=	commodities;
		this.m_index		=	index;
		this.m_all			=	all;
		this.m_dollars		=	dollars;
	}
}
